aString = "The!quick!brown!fox!jumps!over!the!lazy!dog." # Variable has a sentence that has no spaces between words and is combined by "!"

aString = aString.replace("!", " ") # Use the replace function to replace the "!" with spaces using open  inverted commas " "

print(aString) # Print the new sentence without the "!" and with spces between the words.

print(aString.upper()) # Print the sentences in upper case.

print(aString[ : :-1]) # Print the sentences in reverse.
